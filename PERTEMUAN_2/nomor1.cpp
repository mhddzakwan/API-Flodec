#include <iostream> 
using namespace std;

main(){
    int I = 2, K=7, L=1, C=9, U=5, S=6;
    int a,b,c,d;
    
    a = 3-1<I || K-5 < 8+2 && L+7 >= 9+3;
    /* 
    2 < I || 2 < 10 && 8 >= 12;
    0 || 1 && 0
    0 || 0 = 0
    
    */

   b =  L%3>U && (C/U<S || 3*I-K>0);
   /*
   1>U && (1<S || -1>0)
   0 && (1 || 0)
   0 && 1
   0
   */

   c = I-9>K || L+3<4*C;
   /*
   -7>K || 4<36
   0 || 1
   1
   */

    d = I | K & L ^ 3 << 2;
    /*
    3 << 2 = 1100 = 12
    7 & 1 = 111 & 001 = 001 = 1
    1 ^ 12 = 0001 ^ 1100 = 1101 = 13
    2 | 13 = 0010 | 1101 = 1111 = 15

    */

    cout << "a = " << a << endl;
    cout << "b = " << b << endl;
    cout << "c = " << c << endl;
    cout << "d = " << d << endl;
}